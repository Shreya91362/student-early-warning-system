def create_schema(db_path):
    conn = sqlite3.connect(db_path)
    c = conn.cursor()
    c.execute('''CREATE TABLE IF NOT EXISTS students(
        student_id INTEGER PRIMARY KEY, student_name TEXT,
        grade_level INTEGER, enrollment_date TEXT, last_updated TEXT)''')
    c.execute('''CREATE TABLE IF NOT EXISTS academic_records(
        record_id INTEGER PRIMARY KEY AUTOINCREMENT, student_id INTEGER,
        gpa REAL, previous_failures INTEGER DEFAULT 0,
        assignment_completion REAL, last_updated TEXT,
        FOREIGN KEY(student_id) REFERENCES students(student_id))''')
    c.execute('''CREATE TABLE IF NOT EXISTS attendance(
        attendance_id INTEGER PRIMARY KEY AUTOINCREMENT, student_id INTEGER,
        date TEXT, status TEXT, unexcused INTEGER DEFAULT 0,
        FOREIGN KEY(student_id) REFERENCES students(student_id))''')
    c.execute('''CREATE TABLE IF NOT EXISTS behavioral_incidents(
        incident_id INTEGER PRIMARY KEY AUTOINCREMENT, student_id INTEGER,
        total_incidents INTEGER DEFAULT 0, days_since_last INTEGER DEFAULT 365,
        last_updated TEXT, FOREIGN KEY(student_id) REFERENCES students(student_id))''')
    c.execute('''CREATE TABLE IF NOT EXISTS student_demographics(
        demo_id INTEGER PRIMARY KEY AUTOINCREMENT, student_id INTEGER,
        free_reduced_lunch INTEGER DEFAULT 0, extracurricular INTEGER DEFAULT 0,
        parent_contact INTEGER DEFAULT 3,
        FOREIGN KEY(student_id) REFERENCES students(student_id))''')
    c.execute('''CREATE TABLE IF NOT EXISTS counselor_referrals(
        referral_id INTEGER PRIMARY KEY AUTOINCREMENT, student_id INTEGER,
        total_referrals INTEGER DEFAULT 0, last_referral_date TEXT,
        FOREIGN KEY(student_id) REFERENCES students(student_id))''')
    c.execute('CREATE INDEX IF NOT EXISTS idx_att ON attendance(student_id)')
